    <h1>About Crowd Disaster Source Management</h1>
    <p>Crowd Disaster Source Management is a project aimed at leveraging crowd-sourced data and technology to efficiently manage and respond to disasters.</p>
    
    <h2>Objectives</h2>
    <ul>
      <li>Facilitate communication and coordination among disaster responders.</li>
      <li>Enable real-time data collection and analysis to assess the extent of the disaster.</li>
      <li>Provide a platform for individuals to report incidents and request assistance.</li>
      <li>Optimize resource allocation and deployment based on the severity and location of the disaster.</li>
    </ul>

    <h2>Features</h2>
    <ul>
      <li>Interactive maps for visualizing affected areas and resource distribution.</li>
      <li>Reporting tools for individuals to submit incident reports and requests for help.</li>
      <li>Data analytics dashboard for authorities to monitor the situation and make informed decisions.</li>
      <li>Mobile compatibility for on-the-go access and reporting.</li>
    </ul>

    <h2>Team</h2>
